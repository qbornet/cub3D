int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	isneg;

	i = 0;
	res = 0;
	isneg = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			isneg = 1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (isneg)
		res = -res;
	return (res);
}
